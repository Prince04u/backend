const Wallet = require("../models/Wallet");
const Transaction = require("../models/Transaction");
const Deposit = require("../models/Deposit");
const Withdrawal = require("../models/Withdrawal");
const WithdrawalAccount = require("../models/WithdrawalAccount");
const PlatformConfig = require("../models/PlatformConfig");
const logger = require("../config/logger");

const getBalance = async (req, res, next) => {
  try {
    const wallet = await Wallet.findOne({ user: req.user._id });
    return res.json({
      success: true,
      data: {
        balance: wallet ? wallet.balance : 0,
        commissionBalance: wallet ? wallet.commissionBalance : 0,
      },
    });
  } catch (error) {
    return next(error);
  }
};

const getTransactions = async (req, res, next) => {
  try {
    const { type, page = 1, limit = 20 } = req.query;
    const filter = { user: req.user._id };
    if (type) filter.type = type;

    const list = await Transaction.find(filter)
      .sort({ createdAt: -1 })
      .skip((page - 1) * limit)
      .limit(Number(limit));

    return res.json({ success: true, data: list });
  } catch (error) {
    return next(error);
  }
};

const requestDeposit = async (req, res, next) => {
  try {
    const { amount, channel } = req.body;
    if (!amount || !channel) {
      return res.status(400).json({ message: "Amount and channel are required." });
    }

    const config = await PlatformConfig.findOne() || new PlatformConfig();

    let targetAddress = "";
    if (channel === "TRC20" && config.enableTRC20) {
      targetAddress = config.usdt_trc20;
    } else if (channel === "BEP20" && config.enableBEP20) {
      targetAddress = config.usdt_bep20;
    } else if (channel === "ERC20" && config.enableERC20) {
      targetAddress = config.usdt_erc20;
    } else {
      return res.status(400).json({ message: `Deposit channel ${channel} is currently disabled or unsupported.` });
    }

    const deposit = new Deposit({
      user: req.user._id,
      amount,
      channel,
      status: "pending",
    });
    await deposit.save();

    logger.info(`Deposit request generated by ${req.user.mobile} for ₹${amount} via ${channel}`);

    return res.status(201).json({
      success: true,
      data: {
        depositId: deposit._id,
        amount: deposit.amount,
        channel: deposit.channel,
        address: targetAddress,
      },
    });
  } catch (error) {
    return next(error);
  }
};

const uploadDepositProof = async (req, res, next) => {
  try {
    const { txHash, depositId } = req.body;
    const file = req.file;

    // Find the latest pending deposit or matching depositId
    let query = { user: req.user._id, status: "pending" };
    if (depositId) query._id = depositId;

    const deposit = await Deposit.findOne(query).sort({ createdAt: -1 });
    if (!deposit) {
      return res.status(404).json({ message: "No active pending deposit request found." });
    }

    if (txHash) deposit.txHash = txHash;
    if (file) {
      deposit.proofImage = `/uploads/proofs/${file.filename}`;
    }

    await deposit.save();

    logger.info(`Deposit proof submitted for request ID ${deposit._id} (TXID: ${txHash || "Uploaded Image"})`);

    return res.json({ success: true, message: "Payment proof uploaded successfully.", data: deposit });
  } catch (error) {
    return next(error);
  }
};

