const updateWithdrawAccountPreferences = async (req, res, next) => {
  try {
    const { accountId } = req.body;
    if (!accountId) {
      return res.status(400).json({ message: "Preferred account ID is required." });
    }

    await WithdrawalAccount.updateMany({ user: req.user._id }, { isPreferred: false });
    await WithdrawalAccount.findOneAndUpdate({ _id: accountId, user: req.user._id }, { isPreferred: true });

    return res.json({ success: true, message: "Withdrawal preference updated." });
  } catch (error) {
    return next(error);
  }
};

const requestWithdraw = async (req, res, next) => {
  try {
    const { amount, accountId } = req.body;
    if (!amount || !accountId) {
      return res.status(400).json({ message: "Withdrawal amount and account reference are required." });
    }

    const config = await PlatformConfig.findOne() || new PlatformConfig();

    if (amount < config.minWithdraw || amount > config.maxWithdraw) {
      return res.status(400).json({
        message: `Amount must be between ₹${config.minWithdraw} and ₹${config.maxWithdraw}.`,
      });
    }

    const account = await WithdrawalAccount.findOne({ _id: accountId, user: req.user._id });
    if (!account) {
      return res.status(404).json({ message: "Linked withdrawal destination account not found." });
    }

    const wallet = await Wallet.findOne({ user: req.user._id });
    if (!wallet || wallet.balance < amount) {
      return res.status(400).json({ message: "Insufficient wallet balance." });
    }

    const fees = (amount * config.withdrawFeePercent) / 100;
    const netAmount = amount - fees;

    // Deduct balance immediately
    const prevBalance = wallet.balance;
    wallet.balance -= amount;
    await wallet.save();

    const withdrawal = new Withdrawal({
      user: req.user._id,
      amount,
      fees,
      netAmount,
      type: account.type,
      bankName: account.bankName,
      bankCardNumber: account.bankCardNumber,
      bankCardHolder: account.bankCardHolder,
      cryptoAddress: account.cryptoAddress,
      cryptoNetwork: account.cryptoNetwork,
      status: "pending",
    });
    await withdrawal.save();

    // Log ledger txn
    const txn = new Transaction({
      user: req.user._id,
      type: "withdrawal",
      amount,
      direction: "debit",
      prevBalance,
      postBalance: wallet.balance,
      refId: withdrawal._id,
      description: `Withdrawal request of ₹${amount} linked to account ${account.label}`,
    });
    await txn.save();

    logger.info(`Withdrawal request recorded by ${req.user.mobile} for ₹${amount} (Net: ₹${netAmount})`);

    return res.status(201).json({
      success: true,
      message: "Withdrawal request submitted successfully. Processing will take 2-4 hours.",
      data: withdrawal,
    });
  } catch (error) {
    return next(error);
  }
};

const path = require("path");

const fetchDepositProofBlob = async (req, res, next) => {
  try {
    const { id } = req.params;
    const deposit = await Deposit.findById(id);

    if (!deposit) {
      return res.status(404).json({ message: "Deposit request not found." });
    }

    // Restrict non-admins to their own proofs
    if (deposit.user.toString() !== req.user._id.toString() && req.user.role !== "admin") {
      return res.status(403).json({ message: "Access denied." });
    }

    if (!deposit.proofImage) {
      return res.status(404).json({ message: "No payment proof uploaded for this deposit request." });
    }

    const fullPath = path.join(process.cwd(), deposit.proofImage);
    return res.sendFile(fullPath);
  } catch (error) {
    return next(error);
  }
};

