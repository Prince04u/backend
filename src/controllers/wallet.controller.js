const addWithdrawAccount = async (req, res, next) => {
  try {
    const { type, label, bankName, bankCardNumber, bankCardHolder, cryptoAddress, cryptoNetwork } = req.body;

    if (!type) {
      return res.status(400).json({ message: "Withdraw account type (bank/crypto) is required." });
    }

    // Reset preferred status on older accounts if this one is set preferred
    await WithdrawalAccount.updateMany({ user: req.user._id }, { isPreferred: false });

    const account = new WithdrawalAccount({
      user: req.user._id,
      type,
      label: label || `${type.toUpperCase()} Card`,
      bankName,
      bankCardNumber,
      bankCardHolder,
      cryptoAddress,
      cryptoNetwork,
      isPreferred: true,
    });
    await account.save();

    return res.status(201).json({ success: true, message: "Withdrawal account linked.", data: account });
  } catch (error) {
    return next(error);
  }
};

